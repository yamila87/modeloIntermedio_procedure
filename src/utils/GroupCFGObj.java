package utils;

public class GroupCFGObj {

	private String campoPor;
	private String[] camposAgrupar;
	private String campoResultado;
	
	public String getCampoPor() {
		return campoPor;
	}
	public void setCampoPor(String campoPor) {
		this.campoPor = campoPor;
	}
	public String[] getCamposAgrupar() {
		return camposAgrupar;
	}
	public void setCamposAgrupar(String[] camposAgrupar) {
		this.camposAgrupar = camposAgrupar;
	}
	public String getCampoResultado() {
		return campoResultado;
	}
	public void setCampoResultado(String campoResultado) {
		this.campoResultado = campoResultado;
	}
	
}
